package Model;

/**
 *
 * @author thain
 */
public class EnterpriseSizeDetail {
    private int id;
    private String size;

    public EnterpriseSizeDetail() {
    }

    public EnterpriseSizeDetail(int id, String size) {
        this.id = id;
        this.size = size;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
    
    
}
